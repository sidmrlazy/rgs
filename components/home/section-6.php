            <p>In India, the formal pharmacy education - leading to a degree began with the introduction of the 3-year
                bachelors of pharmacy (BPharm) at Banaras Hindu University in the year 1937.</p>

            <p>At that time, the curriculum was presented as a combination of pharmaceutical chemistry, analytical
                chemistry, and pharmacy, which prepared graduates to work as specialists in quality control and
                standardization of drugs for pharmaceutical companies, but not for pharmacy practices!</p>

            <p>Before India gained independence in 1947, there were only 3 institutions offering pharmacy degree
                programs.
                In 1944, the Punjab University started a pharmacy department; in 1947 L.M. College was established in
                Ahmedabad.</p>

            <p>Interestingly, the concept of pharmacy practice was not realized until after independence. In 1948, the
                Pharmacy Act was implemented as the nation's first minimum-standard of educational qualification
                required
                for pharmacy practices to regulate the practice, education, and profession of pharmacy.</p>

            <p>Currently, one needs at least a diploma in pharmacy to practice as a pharmacist. Provisions of the Act
                are
                implemented through the Pharmacy Council of India (PCI).</p>
